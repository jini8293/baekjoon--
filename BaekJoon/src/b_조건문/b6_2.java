package b_조건문;

import java.util.Scanner;

public class b6_2 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        int h = sc.nextInt();
        int m = sc.nextInt();
        int a = sc.nextInt();

        m += a;

        h += m/60;
        m = m%60;

        h = h%24;
        System.out.println(h + " " + m);
    }
}
